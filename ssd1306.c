static void SSD1306_Command(uint8_t command)
{
    I2C_Master_Start();
    I2C_Master_Write(SSD1306_I2C_ADDRESS_WRITE);
    I2C_Master_Write(0x00);
    I2C_Master_Write(command);
    I2C_Master_Stop();
}

static void SSD1306_StartData(void)
{
    I2C_Master_Start();
    I2C_Master_Write(SSD1306_I2C_ADDRESS_WRITE);
    I2C_Master_Write(0x40);
}

static void SSD1306_StopData(void)
{
    I2C_Master_Stop();
}

static void SSD1306_Data(uint8_t data)
{
    I2C_Master_Write(data);
}

void SSD1306_SetCursor(uint8_t column, uint8_t page)
{
    if (column > 127)
    {
        column = 0;
    }

    if (page > 7)
    {
        page = 0;
    }

    oled_column = column;
    oled_page = page;

    SSD1306_Command(0xB0 + page);
    SSD1306_Command(0x00 + (column & 0x0F));
    SSD1306_Command(0x10 + ((column >> 4) & 0x0F));
}

void SSD1306_ClearDisplay(void)
{
    uint8_t page;
    uint8_t column;

    for (page = 0; page < 8; page++)
    {
        SSD1306_SetCursor(0, page);

        SSD1306_StartData();

        for (column = 0; column < 128; column++)
        {
            SSD1306_Data(0x00);
        }

        SSD1306_StopData();
    }

    SSD1306_SetCursor(0, 0);
}

void SSD1306_ClearLine(uint8_t page)
{
    uint8_t column;

    if (page > 7)
    {
        return;
    }

    SSD1306_SetCursor(0, page);

    SSD1306_StartData();

    for (column = 0; column < 128; column++)
    {
        SSD1306_Data(0x00);
    }

    SSD1306_StopData();

    SSD1306_SetCursor(0, page);
}

void SSD1306_WriteChar(char c)
{
    uint8_t i;
    uint16_t index;
    uint8_t character;

    if (c == '\n')
    {
        oled_column = 0;

        if (oled_page < 7)
        {
            oled_page++;
        }
        else
        {
            oled_page = 0;
        }

        SSD1306_SetCursor(oled_column, oled_page);
        return;
    }

    character = (uint8_t)c;

    if (character < 32 || character > 127)
    {
        character = '?';
    }

    if (oled_column > 122)
    {
        oled_column = 0;

        if (oled_page < 7)
        {
            oled_page++;
        }
        else
        {
            oled_page = 0;
        }

        SSD1306_SetCursor(oled_column, oled_page);
    }

    index = ((uint16_t)(character - 32)) * 5;

    SSD1306_StartData();

    for (i = 0; i < 5; i++)
    {
        SSD1306_Data(Font5x7[index + i]);
    }

    SSD1306_Data(0x00);

    SSD1306_StopData();

    oled_column += 6;
}

void SSD1306_WriteString(const char *str)
{
    while (*str)
    {
        SSD1306_WriteChar(*str);
        str++;
    }
}

void SSD1306_Init(void)
{
    __delay_ms(100);

    SSD1306_Command(0xAE);  // Display OFF

    SSD1306_Command(0x20);  // Memory addressing mode
    SSD1306_Command(0x02);  // Page addressing mode

    SSD1306_Command(0xB0);  // Page start address
    SSD1306_Command(0xC8);  // COM output scan direction remapped
    SSD1306_Command(0x00);  // Low column address
    SSD1306_Command(0x10);  // High column address
    SSD1306_Command(0x40);  // Start line address

    SSD1306_Command(0x81);  // Contrast
    SSD1306_Command(0x7F);

    SSD1306_Command(0xA1);  // Segment remap
    SSD1306_Command(0xA6);  // Normal display

    SSD1306_Command(0xA8);  // Multiplex ratio
    SSD1306_Command(0x3F);

    SSD1306_Command(0xA4);  // Display follows RAM

    SSD1306_Command(0xD3);  // Display offset
    SSD1306_Command(0x00);

    SSD1306_Command(0xD5);  // Display clock divide
    SSD1306_Command(0x80);

    SSD1306_Command(0xD9);  // Pre-charge period
    SSD1306_Command(0xF1);

    SSD1306_Command(0xDA);  // COM pins hardware configuration
    SSD1306_Command(0x12);

    SSD1306_Command(0xDB);  // VCOMH deselect level
    SSD1306_Command(0x40);

    SSD1306_Command(0x8D);  // Charge pump
    SSD1306_Command(0x14);

    SSD1306_Command(0xAF);  // Display ON

    SSD1306_ClearDisplay();
}
